function crystalCommon(formID, ajaxSubmit) {
	var crystalInstance = Crystal('contact-us', true);
	
	// name field validation 
	crystalInstance.addField(/[A-Za-z -']$/ , document.getElementById('crystal-someName'));

	// email validation... will validate all emails that adhere to the RFC2822 standard.
	var re = /^([^\x00-\x20\x22\x28\x29\x2c\x2e\x3a-\x3c\x3e\x40\x5b-\x5d\x7f-\xff]+|\x22([^\x0d\x22\x5c\x80-\xff]|\x5c[\x00-\x7f])*\x22)(\x2e([^\x00-\x20\x22\x28\x29\x2c\x2e\x3a-\x3c\x3e\x40\x5b-\x5d\x7f-\xff]+|\x22([^\x0d\x22\x5c\x80-\xff]|\x5c[\x00-\x7f])*\x22))*\x40([^\x00-\x20\x22\x28\x29\x2c\x2e\x3a-\x3c\x3e\x40\x5b-\x5d\x7f-\xff]+|\x5b([^\x0d\x5b-\x5d\x80-\xff]|\x5c[\x00-\x7f])*\x5d)(\x2e([^\x00-\x20\x22\x28\x29\x2c\x2e\x3a-\x3c\x3e\x40\x5b-\x5d\x7f-\xff]+|\x5b([^\x0d\x5b-\x5d\x80-\xff]|\x5c[\x00-\x7f])*\x5d))*$/;
	crystalInstance.addField(re, document.getElementById('crystal-email'))

	// message validation... will match everything but an empty message.
	crystalInstance.addField(/^(?!\s*$).+/ , document.getElementById('crystal-message'))

	return crystalInstance;
}